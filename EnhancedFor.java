public class EnhancedFor {
    public static void main(String[] args) {
        char[] alfabet = { 'a', 'b', 'c', 'd', 'e', 'f', 'g' };

        for (char huruf : alfabet) {
            System.out.print(huruf + " ");
        }

        /*
         * Tujuan dari kode di atas adalah untuk mengprint setiap elemen yang terdapat
         * dalam array alfabet. Syntax yang digunakan adalah enhanced-for atau foreach
         * untuk mengprint setiap elemen yang ada dalam array alfabet. Output yang
         * dihasilkan adalah "a b c d e f g ".
         */
    }
}
